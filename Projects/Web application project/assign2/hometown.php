						<section id="travellingguide">
							<h1>Travelling Guide</h1>
							<p>I will be showing you the modest and efficient way to travel Mustang from Kathmandu Valley, city of temples and only city in whole damn world where seven of the preserved world heritage lies. Starting from the go, after relaxing in Kathmandu you can start your journey. Contacting travels and tours might be expensive also a hectic strict schedule you need to follow. The following guide may help a lot for domestic as well as international tourists. These are</p>
							<h2>Step by step guide</h2>
							<ul>
								<li>The first step, visit Gangabu area in Kathmandu where you can buy tickets from Kathmandu to Beni. In-between these two locations Kathmandu to Beni lies Pokhara, city of lakes, beautiful valley must visit but i would suggest you to visit Pokhara returning from your tour. It is best you travel at night because you must reach Beni in morning time. Or the very next morning if you travel by day bus to Beni. You can relax a day in Beni if you have enough time to travel. (Rs 700-1000)</li>
								<li>It is very difficult to find tickets to Ghasa, a village starting of the Mustang district or Jomsom, Headquarter of Mustang district. If you reach Beni by 6 AM you can easily get ticket to Ghasa or Jomsom depending on the Bus you choose.(Rs 400-500 Beni to Ghasa or Rs 700-1000 Beni to Jomsom)(Nepali Khana 300-350 at Ghasa Rs 100-200 per bed)</li>
								<li>The bus can take you to Ghasa if you get bus to Ghasa by 1 PM. This is very beautiful village to stay. There are plenty of lodge to stay if you are willing to stay. By reaching there ask for or wait the Bus to jomsom and you can reach Jomsom by 6 PM, a headquarter where cool air blows day and night. I suggest you to carry masks as much as possible to protect yourself. A very good place to spend night. (Nepali Khana 300-350 at Jomsom Rs 100-200 per bed)</li>
								<li>Next Morning ask for ticket counter where you can get a bus to Muktinath or trek up to Muktinath, I bet you you wont regret this. You will get chance to trek some beautiful places such as Kagbeni and on the way many.(Nepali Khana 400-500 at Muktinath Rs 200-300 per bed)</li>
								<li>If you trek relax for night at Muktinath and enjoy Muktinath, a holy temple tomorrow.</li>
								<li>This is only lower Mustang. If you want more ask for local people for route for other beautiful places such as Lo Manthang.</li>
								<li>Returning donot miss these sites Dhumpa lake at Jomsom, Marpha the apple farm, Chahara near Ghasa , deepest gorge in the world , Tatopani and pokhara</li>
								<li>If you have more time, do trekking returning to enjoy and explore the most beautiful country in the world. If you want to enjoy more trekking, ask for route to poon hill (above 3500 m from sea level). It will take another 5-6 days.</li>
							</ul>
